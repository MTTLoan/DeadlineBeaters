    private void jButton_LuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_LuuActionPerformed
        // TODO add your handling code here:
        String MaHDP = jTextField_MaHDP.getText();
        String MaXe = jTextField_MaXe.getText();
        String LoaiPhat = (String) jComboBox_LoaiPhat.getSelectedItem();
        String SoTien = jTextField_SoTien.getText();
        String ChuThich = jTextArea_ChuThich.getText();
        
        
        try {
            if( MaHDP.isEmpty() || MaXe.isEmpty() || LoaiPhat.isEmpty() || ChuThich.isEmpty() || SoTien.isEmpty()){
                JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            }else{
                ChiTietHDPhat cthdp = new ChiTietHDPhat(Integer.parseInt(MaHDP), Integer.parseInt(MaXe), LoaiPhat, Integer.parseInt(SoTien), null, ChuThich);
                if(ImgPath != null)
                    ChiTietHDPhatDAO.getInstance().update(cthdp, ImgPath);
                else
                    ChiTietHDPhatDAO.getInstance().update(cthdp);
                this.dispose();
                JOptionPane.showMessageDialog(this, "Sửa thành công!");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Số tiền không hợp lệ!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(ThemCTHDPhat.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton_LuuActionPerformed

    private void jButton_ChonHinhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ChonHinhActionPerformed
        // TODO add your handling code here:
        JFileChooser file = new JFileChooser();
        file.setCurrentDirectory(new File(System.getProperty("user.home")));

        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.images", "jpg","png");
        file.addChoosableFileFilter(filter);
        int result = file.showSaveDialog(null);
        if(result == JFileChooser.APPROVE_OPTION)
        {
            File selectedFile = file.getSelectedFile();
            String path = selectedFile.getAbsolutePath();
            jLabel_Hinh.setIcon(ResizeImage(path, null));
            ImgPath = path;
        }
        else{
            System.out.println("No File Selected");
        }
    }//GEN-LAST:event_jButton_ChonHinhActionPerformed

    // hàm chỉnh kích thước hình ảnh theo chiều dài và chiều rộng của jLabel_Hinh
    public ImageIcon ResizeImage(String imagePath, byte[] pic)
    {
        ImageIcon myImage = null;

        if(imagePath != null)
        {
            myImage = new ImageIcon(imagePath);
        }else{
            myImage = new ImageIcon(pic);
        }

        Image img = myImage.getImage();
        Image img2 = img.getScaledInstance(jLabel_Hinh.getWidth(), jLabel_Hinh.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon image = new ImageIcon(img2);
        return image;

    }
    
